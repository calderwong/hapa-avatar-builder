export const MAJOR_ARCANA_REFERENCES = MAJOR_ARCANA_COLOR_REFERENCE.filter((entry) => entry.arcana);

export function cardReferencePatch(entry) {
  if (!entry?.arcana) return null;
  return {
    title: entry.arcana.title,
    number: entry.no,
    arcana: "major",
    keywords: entry.arcana.keywords,
    meaning: entry.arcana.upright,
    reversedMeaning: entry.arcana.reversed,
    promptNotes: [
      `Color identity: ${entry.color} / ${entry.hex} / RGB(${entry.rgb.join(", ")})`,
      entry.arcana.creationNotes
    ].join("\n\n")
  };
}
